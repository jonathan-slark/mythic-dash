// --- Maze functions ---

bool game__isMazeWall(float x, float y) {
  // Out of bounds
  if (x < 0.0f || x > MAZE_SIZE.x || y < 0.0f || y > MAZE_SIZE.y) {
    return true;
  }

  int row = (int) (y / TILE_SIZE);
  int col = (int) (x / TILE_SIZE);

  assert(row >= 0 && row < MAZE_ROWS);
  assert(col >= 0 && col < MAZE_COLS);

  return !maze[row][col];
}
